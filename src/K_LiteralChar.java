public class K_LiteralChar {
    public static void main(String[] args) {
        char tab = '\t';
        char newline = '\n';
        char singleQuote = '\'';
        char doubleQuote = '\"';
        System.out.println("Ini contoh" + tab + "literal tab");
        System.out.println("Ini contoh" + newline + "literal newline");
        System.out.println("" + singleQuote + "Ini contoh single quote" + singleQuote);
        System.out.println("" + doubleQuote + "Ini contoh literal double quote" + doubleQuote);


    }
}
