public class O_Ekspresi { //kombinasi dari operator, fungsi, nilai literal, variable atau konstanta dan selalu merubah nilai suatu variable
    public static void main(String[] args) {
        int bilA = 5;
        int bilB = bilA * 2;
        int bilC = bilA +bilB;
        System.out.println("Ini contoh ekspresi : bilA * 2 = " +bilB);
        System.out.println("Ini contoh ekspresi : bilA + bilB = " + bilC);
    }
}
